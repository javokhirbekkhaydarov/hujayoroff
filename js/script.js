// ========== SLIDER FUNCTIONALITY ==========
const slider = document.getElementById("slider");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");
const dots = document.querySelectorAll(".dot");
const slides = document.querySelectorAll(".t1148__item");

if (slider && slides.length > 0) {
  let currentIndex = 0;
  let autoScrollInterval;
  let isDragging = false;
  let startX;
  let scrollLeft;

  function getVisibleSlides() {
    if (window.innerWidth <= 768) return 1;
    if (window.innerWidth <= 1024) return 2;
    return 3;
  }

  function getSlideWidth() {
    const sliderStyle = getComputedStyle(slider);
    const gap = parseInt(sliderStyle.gap) || 20;
    const containerWidth = slider.clientWidth;
    const visibleSlides = getVisibleSlides();
    return (containerWidth - gap * (visibleSlides - 1)) / visibleSlides;
  }

  function updateActiveDot(index) {
    dots.forEach((dot, i) => {
      if (i === index) {
        dot.classList.add("active");
      } else {
        dot.classList.remove("active");
      }
    });
  }

  function scrollToSlide(index) {
    const slideWidth = getSlideWidth();
    const gap = 20;
    const scrollPosition = index * (slideWidth + gap);
    slider.scrollTo({
      left: scrollPosition,
      behavior: "smooth",
    });
    updateActiveDot(index);
    currentIndex = index;
  }

  function getCurrentSlideIndex() {
    const slideWidth = getSlideWidth();
    const gap = 20;
    const scrollPosition = slider.scrollLeft;
    let index = Math.round(scrollPosition / (slideWidth + gap));
    index = Math.max(0, Math.min(index, slides.length - 1));
    return index;
  }

  function nextSlide() {
    if (currentIndex < slides.length - 1) {
      scrollToSlide(currentIndex + 1);
    } else {
      scrollToSlide(0);
    }
  }

  function prevSlide() {
    if (currentIndex > 0) {
      scrollToSlide(currentIndex - 1);
    } else {
      scrollToSlide(slides.length - 1);
    }
  }

  function startAutoScroll() {
    if (autoScrollInterval) clearInterval(autoScrollInterval);
    autoScrollInterval = setInterval(() => {
      if (!isDragging) {
        nextSlide();
      }
    }, 5000);
  }

  function stopAutoScroll() {
    if (autoScrollInterval) {
      clearInterval(autoScrollInterval);
      autoScrollInterval = null;
    }
  }

  function handleSliderScroll() {
    const newIndex = getCurrentSlideIndex();
    if (newIndex !== currentIndex) {
      currentIndex = newIndex;
      updateActiveDot(currentIndex);
    }
  }

  // Slider event listeners
  slider.addEventListener("scroll", handleSliderScroll);

  slider.addEventListener("mousedown", (e) => {
    isDragging = true;
    startX = e.pageX - slider.offsetLeft;
    scrollLeft = slider.scrollLeft;
    stopAutoScroll();
  });

  slider.addEventListener("mouseleave", () => {
    isDragging = false;
    startAutoScroll();
  });

  slider.addEventListener("mouseup", () => {
    isDragging = false;
    startAutoScroll();
  });

  slider.addEventListener("mousemove", (e) => {
    if (!isDragging) return;
    e.preventDefault();
    const x = e.pageX - slider.offsetLeft;
    const walk = (x - startX) * 2;
    slider.scrollLeft = scrollLeft - walk;
  });

  slider.addEventListener("touchstart", (e) => {
    isDragging = true;
    startX = e.touches[0].pageX - slider.offsetLeft;
    scrollLeft = slider.scrollLeft;
    stopAutoScroll();
  });

  slider.addEventListener("touchend", () => {
    isDragging = false;
    startAutoScroll();
  });

  slider.addEventListener("touchmove", (e) => {
    if (!isDragging) return;
    const x = e.touches[0].pageX - slider.offsetLeft;
    const walk = (x - startX) * 2;
    slider.scrollLeft = scrollLeft - walk;
  });

  if (prevBtn)
    prevBtn.addEventListener("click", () => {
      prevSlide();
      stopAutoScroll();
      startAutoScroll();
    });
  if (nextBtn)
    nextBtn.addEventListener("click", () => {
      nextSlide();
      stopAutoScroll();
      startAutoScroll();
    });

  dots.forEach((dot, index) => {
    dot.addEventListener("click", () => {
      scrollToSlide(index);
      stopAutoScroll();
      startAutoScroll();
    });
  });

  let resizeTimeout;
  window.addEventListener("resize", () => {
    clearTimeout(resizeTimeout);
    resizeTimeout = setTimeout(() => {
      scrollToSlide(currentIndex);
    }, 150);
  });

  // Initialize slider
  scrollToSlide(0);
  startAutoScroll();

  // Fix for Safari momentum scrolling
  slider.style.webkitOverflowScrolling = "touch";
}

