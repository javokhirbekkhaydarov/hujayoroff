// Lazy loading images
const images = document.querySelectorAll(".t1148__img");
const imageObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        const img = entry.target;
        img.style.opacity = "1";
        imageObserver.unobserve(img);
      }
    });
  },
  { threshold: 0.1 },
);

